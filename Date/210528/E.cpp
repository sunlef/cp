typedef int __QREADTYPE;
inline __QREADTYPE READ()
{
	__QREADTYPE x = 0, f = 1;
	char ch = getchar();
	while(ch < '0' || ch > '9') {
		if(ch == '-')
			f = -1;
		ch = getchar();
	}
	while(ch >= '0' && ch <= '9') {
		x = 10 * x + ch - '0';
		ch = getchar();
	}
	return f * x;
}
